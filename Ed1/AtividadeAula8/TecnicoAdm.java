public class TecnicoAdm {
    private String nomeTec;
    private int siapeTec;

    public TecnicoAdm(String nomeTec, int siapeTec) {
        this.nomeTec = nomeTec;
        this.siapeTec = siapeTec;
    }
    public String getnomeTec() {
        return nomeTec;
    }
    public int getSiape() {
        return siapeTec;
    }
    
}
